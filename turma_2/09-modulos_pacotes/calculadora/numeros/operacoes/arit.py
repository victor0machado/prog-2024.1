"""
Operações aritméticas simples
"""

def soma(a, b):
    return a + b

def subtracao(a, b):
    return a - b

def multiplicacao(a, b):
    return a * b

def divisao(a, b):
    return a / b

if __name__ == "__main__":
    print(soma(10, 2)) # 12
    print(subtracao(10, 2)) # 8
    print(multiplicacao(10, 2)) # 20
    print(divisao(10, 2)) # 5.0
